#include <stdio.h>

int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}

int	main()
{
	int	i;
	int	c = '2';

	i = ft_isalpha(c);
	printf("%d", i);
}
